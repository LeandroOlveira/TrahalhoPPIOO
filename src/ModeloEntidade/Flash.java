package modeloEntidade;


public class Flash extends Antagonista{

	public Flash(){
		super();
		quantidadeVida = 150;
		dano = 15;
	}
	
	@Override
	public String toString(){
		return "nome: " + nome + "\n" 
			  +"Quantidade de Vida: " + quantidadeVida + "\n"
			  +"Poder de Dano: " + dano + "\n\n";
	}
	
	@Override
	public int getDano() {
		return dano;
	}
	

}
