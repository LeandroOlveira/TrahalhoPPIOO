package modeloEntidade;


public class Batman extends Antagonista {

	
	public Batman(){
		super();
		quantidadeVida = 210;
		nome = "Batman";
		dano = 21;
	}
	@Override
	public String toString(){
		return "nome: " + nome + "\n" 
			  +"Quantidade de Vida: " + quantidadeVida + "\n"
			  +"Poder de Dano: " + dano + "\n\n";
	}

	@Override
	public int getDano() {
		return dano;
	}

}
